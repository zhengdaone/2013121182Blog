package DataBean;

public class pReply {
	private int pReplyId;
	private int pCommentId;
	private String reContent;
	private String reUser;
	public int getpReplyId() {
		return pReplyId;
	}
	public void setpReplyId(int pReplyId) {
		this.pReplyId = pReplyId;
	}
	public int getpCommentId() {
		return pCommentId;
	}
	public void setpCommentId(int pCommentId) {
		this.pCommentId = pCommentId;
	}
	public String getReContent() {
		return reContent;
	}
	public void setReContent(String reContent) {
		this.reContent = reContent;
	}
	public String getReUser() {
		return reUser;
	}
	public void setReUser(String reUser) {
		this.reUser = reUser;
	}
	
}
